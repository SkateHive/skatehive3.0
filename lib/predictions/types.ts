// Types for the HivePredict integration (issue #192).
// Field names mirror the hivepredict.app public API response shapes verbatim
// so responses can be used without remapping.

export type MarketToken = "HIVE" | "HBD";

// "YES" / "NO" for binary markets; "O1".."On" for multi-outcome markets.
// Kept as a plain string so both kinds flow through the same code paths.
export type MarketOutcome = string;

export type MarketStatus =
  | "pending"
  | "active"
  | "resolved"
  | "voided"
  | "cancelled";

export interface Market {
  id: string;
  onChainId?: string;
  creatorUsername?: string;
  creatorSide?: MarketOutcome;
  title: string;
  description?: string;
  category?: string;
  token: MarketToken;
  outcomes: MarketOutcome[];
  outcomeLabels?: Record<string, string>;
  stakeCap?: string | number;
  minParticipants?: number;
  bettingClosesAt?: string;
  resolvesAt?: string;
  status: MarketStatus;
  totalPool?: string;
  // Binary markets populate yes/no; multi-outcome markets populate
  // outcomePools keyed by outcome code (O1..On), with yes/no = "0".
  yesPool?: string;
  noPool?: string;
  outcomePools?: Record<string, string>;
  createdAt?: string;
}

export interface MarketListResponse {
  markets: Market[];
  total: number;
  page: number;
  limit: number;
}

export interface Prediction {
  id: string;
  marketId: string;
  marketOnChainId?: string;
  hiveUsername: string;
  amount: string;
  remainingAmount?: string;
  token: MarketToken;
  outcome: string;
  txId?: string;
  status?: string;
  isCreator?: boolean;
  blockNum?: number;
  createdAt?: string;
}

export interface TokenBalance {
  account?: string;
  username?: string;
  symbol: MarketToken;
  balance: string;
  // e.g. "native-unsupported" when the API cannot read a native HIVE/HBD
  // balance — in that case `balance` is not authoritative.
  source?: string;
}

export type ResolutionType = "manual" | "auto";

// Fields a user supplies when creating a market. `marketId` is generated by
// the caller (see `newMarketId`) so the op-builder stays pure and deterministic.
// Generic over any outcome set: binary (YES/NO), multi (O1..On), or sports.
export interface CreateMarketFields {
  title: string;
  description: string;
  category: string;
  token: MarketToken;
  outcomes: MarketOutcome[]; // ["YES","NO"] or ["O1".."On"]
  outcomeLabels: Record<string, string>;
  creatorSide: MarketOutcome; // one of `outcomes`
  stakeCap: number;
  minParticipants: number;
  resolutionType: ResolutionType;
  // For auto markets, e.g. "oddsapi:<sportKey>:<eventId>". null for manual.
  resolutionSource: string | null;
  resolutionCriteria: string;
  bettingClosesAt: string; // ISO
  resolvesAt: string; // ISO
  openingBetAmount: number;
}

// A single upcoming sports event from /api/sports/:league/events.
export interface SportsEvent {
  id: string;
  sportKey: string;
  homeTeam: string;
  awayTeam: string;
  commenceTime: string;
}

export type LeaderboardBoard = "creators" | "profit" | "accuracy" | "volume";

// Entry shape varies by board; common fields plus board-specific metrics.
export interface LeaderboardEntry {
  rank: number;
  username: string;
  marketsCreated?: number;
  volumeAttracted?: string;
  earnings?: string;
  staked?: string;
  profit?: string;
  wins?: number;
  resolvedPredictions?: number;
  winRate?: string;
  volume?: string;
  predictionCount?: number;
}

export interface LeaderboardResponse {
  board: LeaderboardBoard;
  period: string;
  entries: LeaderboardEntry[];
}
